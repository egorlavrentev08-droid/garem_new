def is_admin(user_id: int) -> bool:
    """Проверяет, является ли пользователь админом"""
    return user_id in ADMIN_IDS

def is_allowed_chat(chat_id: int) -> bool:
    """Проверяет, разрешён ли чат для работы бота"""
    return chat_id in [CHAT_ID, ADMIN_CHAT_ID]

def get_rank_by_streak(streak: int) -> dict:
    """Возвращает ранг по текущему стрику"""
    for rank in reversed(RANKS):
        if streak >= rank["min_streak"]:
            return rank
    return RANKS[0]  # Без ранга
